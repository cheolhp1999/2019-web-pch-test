// toggle message in on icon
$(".fa-user").mouseover(function(){
		$(this).next().css({
			"opacity":1, "transform":"translateY(-10px)"
		})
})
$(".fa-user").mouseleave(function(){
		$(this).next().css({
			"opacity":0, "transform":"translateY(10px)"
		})
})

